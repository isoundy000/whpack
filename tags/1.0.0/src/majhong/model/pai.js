majhong.Pai = cc.Class.extend({
    ctor: function (pai) {  // F1 B8 等字符串
        this.object = {};
        if (typeof(pai) == 'string') {
            this.type = pai[0];  // 牌类型  J F W B T
            this.value = parseInt(pai[1]);    // 牌面值
            this.key = pai;
            this.object["type"] = this.type;
            this.object["value"] = this.value;

        } else {
            this.type = pai.type;
            this.value = pai.value;
            this.key = this.type+""+this.value;
            this.object = pai;
        }

        this.num = majhong.PaiFace[this.key];
    },
    objectOfPai: function () {
      return this.object;
    },

    keyOfPai: function () {
      return this.key;
    },

    numOfPai: function () {
      return this.num;
    },
    YCnumOfPai: function () {
        if(this.num == 40)
        {
            if(MajhongInfo.GameMode == GameMode.PLAY)
            {
                return majhong.PaiFace[ycmajhong.table.JinPaiId];
            }else
            {
                return majhong.PaiFace[ycmajhong.record.JinPaiId];
            }
        }
        return this.num;
    },
    //********quanzhou*********
    isHuaPai: function () {
        return  this.key.indexOf("H") >= 0;
    },
    //---------quanzhou-----------

    isQiDongHuaPai:function()
    {
        return  this.key.indexOf("H") >= 0 || this.key.indexOf("J") >= 0;
    },

    isBaiDaHuaPai:function()
    {
        return  this.key.indexOf("H") >= 0;
    },

    frameImgStandOfPai:function(){
        return majhong.PaiFace.ImgOfStand(this.num);
    },

    frameImgDownOfPai:function(){
        return majhong.PaiFace.ImgOfStand(this.num);
    },

    frameImgDownDesk:function(){
        return majhong.PaiFace.ImgOfDown(this.num);
    },

    frameImgRightShowOfPai:function(){
        return majhong.PaiFace.ImgOfRight(this.num);
    },

    frameImgLeftShowOfPai:function(){
        return majhong.PaiFace.ImgOfLeft(this.num);
    },

    frameImgUpShowOfPai:function(){
        return majhong.PaiFace.ImgOfUp(this.num);
    },

    frameImgUpBackOfPai:function(idx){
        return majhong.PaiFace.BackOfUp(idx);
    },

    frameImgRightBackOfPai:function(){
        return majhong.PaiFace.BackOfStandLeft();
    },

    frameImgLeftBackOfPai:function(){
        return majhong.PaiFace.BackOfStandLeft();
    },

    frameImgDownBackOfPai:function(){
        return majhong.PaiFace.BackOfDown();
    },
});

